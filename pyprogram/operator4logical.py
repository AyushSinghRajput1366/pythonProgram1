# logical operator
x = 5
y = 10
#and ke andr jitana bhi condition ho true hone chahiye tabhi Output trur ayegi

print(x==5 and x<y and x == y) # x==y is wrong condition

#or ke andr ager ek bhi condition true hogi to wo true result dega
print(x==5 or x<y or x == y)

print(x!=5)
# if we put not before the cond it will reverse the answer
print(not x!=5)







